#!/bin/bash

echo "Local Phone numbers:"
echo "--------------------------------------"
egrep -o '[0-9][0-9][0-9]-[0-9][0-9][0-9][0-9]' lorem.txt
echo "--------------------------------------"

echo "Phone numbers with optional area code:"
echo "--------------------------------------"
egrep -o '(\([0-9][0-9][0-9]\) )?[0-9][0-9][0-9]-[0-9][0-9][0-9][0-9]' lorem.txt
echo "--------------------------------------"

echo "Email addresses:"
echo "--------------------------------------"
egrep -o '[a-zA-Z0-9._]+@[a-zA-Z0-9_]+(\.[a-zA-Z0-9_]+)+' lorem.txt
echo "--------------------------------------"
